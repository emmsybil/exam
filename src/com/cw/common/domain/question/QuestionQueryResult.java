package com.cw.common.domain.question;

/**
 * 试题查询结果实体类
 * @author yuanguangjie
 *
 */
public class QuestionQueryResult {

	private int questionId; //试题编号
	private String content; //试题内容
	private String answer; //答案
	private String analysis;  //分析
	private int questionTypeId; //试题类型编号
	private String referenceName; //参考名称
	private String pointName; //知识点名称
	private String fieldName; //题库名称
	private float questionPoint; //试题分数
	private String examingPoint; 
	private int knowledgePointId; //知识点编号
	public int getKnowledgePointId() {
		return knowledgePointId;
	}
	public void setKnowledgePointId(int knowledgePointId) {
		this.knowledgePointId = knowledgePointId;
	}
	public String getExamingPoint() {
		return examingPoint;
	}
	public void setExamingPoint(String examingPoint) {
		this.examingPoint = examingPoint;
	}
	public float getQuestionPoint() {
		return questionPoint;
	}
	public void setQuestionPoint(float questionPoint) {
		this.questionPoint = questionPoint;
	}
	public int getQuestionTypeId() {
		return questionTypeId;
	}
	public void setQuestionTypeId(int questionTypeId) {
		this.questionTypeId = questionTypeId;
	}
	public int getQuestionId() {
		return questionId;
	}
	public void setQuestionId(int questionId) {
		this.questionId = questionId;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	public String getAnalysis() {
		return analysis;
	}
	public void setAnalysis(String analysis) {
		this.analysis = analysis;
	}
	public String getReferenceName() {
		return referenceName;
	}
	public void setReferenceName(String referenceName) {
		this.referenceName = referenceName;
	}
	public String getPointName() {
		return pointName;
	}
	public void setPointName(String pointName) {
		this.pointName = pointName;
	}
	public String getFieldName() {
		return fieldName;
	}
	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}
}
