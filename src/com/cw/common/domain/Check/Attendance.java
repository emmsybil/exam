package com.cw.common.domain.Check;

import java.util.List;

/**
 * 考勤详情表
 * 
 * @author 文浩
 *
 */
public class Attendance {
	// 表Id
	private int id;
	// 人员Id
	private int userId;
	// 上午打卡时间
	private String AMDate;
	// 下午打卡时间
	private String PMDate;
	// 晚上打卡时间
	private String EMDate;
	// 考勤日期
	private String checkDate;
	// 学生姓名
	private String studentName;
	// 迟到次数
	private int beLateNum;
	// 旷课次数
	private int absenteeismSum;
	// 考勤详情
	private List<AttendanceType> list;

	public int getBeLateNum() {
		return beLateNum;
	}

	public void setBeLateNum(int beLateNum) {
		this.beLateNum = beLateNum;
	}

	public int getAbsenteeismSum() {
		return absenteeismSum;
	}

	public void setAbsenteeismSum(int absenteeismSum) {
		this.absenteeismSum = absenteeismSum;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getAMDate() {
		return AMDate;
	}

	public void setAMDate(String aMDate) {
		AMDate = aMDate;
	}

	public String getPMDate() {
		return PMDate;
	}

	public void setPMDate(String pMDate) {
		PMDate = pMDate;
	}

	public String getEMDate() {
		return EMDate;
	}

	public void setEMDate(String eMDate) {
		EMDate = eMDate;
	}

	public String getCheckDate() {
		return checkDate;
	}

	public void setCheckDate(String checkDate) {
		this.checkDate = checkDate;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public List<AttendanceType> getList() {
		return list;
	}

	public void setList(List<AttendanceType> list) {
		this.list = list;
	}

}
